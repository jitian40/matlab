%总体方向图函数
function F=f(theta,phi)%参数包含球坐标下theta角与phi角，激励相位的初始位置，激励相位数组
    phi_Z=[0,90,180,270];%单元分布角度
    d_busbar=[0,15,30,45];%单元分布层数
    R=40;%底面圆半径
    Phi_Y=atand(2);%椎体底角
%     Phi_Y=pi/2;%圆柱体
    e=zeros(180,180);
        for Ai=1:length(phi_Z)%确定层数
         zarc=phi_Z(Ai);%柱坐标下phi角
            for Zi=1:length(d_busbar)%母线上单元方向图的叠加
                d_z=2*d_busbar(Zi)./sqrt(5);%计算Zn
                e=e+xchange(zarc,Phi_Y,theta,phi,d_z,R);%方向图函数的复数域的叠加(matlab可以直接计算复数)
            end
        end
      F=abs(e)./max(max(abs(e)));%总体方向图函数归一化
end